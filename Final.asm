INCLUDE macros2.asm
INCLUDE number.asm

.MODEL LARGE
.386
.STACK 200h

TRUE		EQU		1
FALSE		EQU		0
MAXTEXTSIZE	EQU		320

.DATA
	a		dd		?
	b		dd		?
	c		dd		?
	d		dd		?
	e		db		MAXTEXTSIZE dup (?), '$'
	h		db		MAXTEXTSIZE dup (?), '$'
	f		dd		?
	g		dd		?
	_3		dd		3
	_5		dd		5
	_1		dd		1
	_2		dd		2
	_NEG_525		dd		-525
	_T_RESULTADO_DE_B		db		"RESULTADO DE B", '$', 304 dup (?)
	auxR0	DD	0.0
	auxR1	DD	0.0
	auxE0	DW	0
	auxE1	DW	0

.CODE
.startup
	MOV AX,@DATA
	MOV DS,AX
;ASIGNACION ENTERA
	FILD	_3
	FISTP	a
;ASIGNACION ENTERA
	FILD	_5
	FISTP	b
ET_6:
	FILD	_1
	FILD	a
	FCOMP
	FSTSW	AX
	FWAIT
	SAHF
	JBE	ET_42
ET_12:
	FILD	_1
	FILD	b
	FCOMP
	FSTSW	AX
	FWAIT
	SAHF
	JBE	ET_34
	FILD	_2
	FILD	b
	FCOMP
	FSTSW	AX
	FWAIT
	SAHF
	JNE	ET_26
;ASIGNACION ENTERA
	FILD	_NEG_525
	FISTP	b
ET_26:
;RESTA DE ENTEROS
	FILD	_1
	FILD	b
	FSUBR
	FISTP	auxE0
;ASIGNACION ENTERA
	FILD	auxE0
	FISTP	b
	JMP	ET_12
ET_34:
;RESTA DE ENTEROS
	FILD	_1
	FILD	a
	FSUBR
	FISTP	auxE1
;ASIGNACION ENTERA
	FILD	auxE1
	FISTP	a
	JMP	ET_6
ET_42:
;SALIDA POR CONSOLA
	DisplayString	_T_RESULTADO_DE_B
	NewLine 1
;SALIDA POR CONSOLA
	DisplayInteger	b,3
	NewLine 1

MOV	AX, 4C00H
INT	21H
END